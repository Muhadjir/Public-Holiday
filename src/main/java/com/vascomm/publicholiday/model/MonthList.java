package com.vascomm.publicholiday.model;

public class MonthList {

	private int id;
	
	private String name;
	
	public MonthList() {
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
